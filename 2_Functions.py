import turtle

shayk_turtle = turtle.Turtle()


def square():
    shayk_turtle.forward(100)
    shayk_turtle.right(90)
    shayk_turtle.forward(100)
    shayk_turtle.right(90)
    shayk_turtle.forward(100)
    shayk_turtle.right(90)
    shayk_turtle.forward(100)
    shayk_turtle.right(90)


square()
shayk_turtle.forward(200)
square()
